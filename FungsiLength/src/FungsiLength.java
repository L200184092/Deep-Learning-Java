public class FungsiLength {
    public static void main(String[] args){
        String nama = "Adam";

        // Menampung jumlah karakter dari nama
        int jumlahKarakter = nama.length();

        System.out.println("'" + nama + "' mempunyai jumlah karakter " +
                "sebanyak " + jumlahKarakter);
    }
}
